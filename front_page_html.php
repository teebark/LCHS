			<p>Founded in 1966, the Louisa County Historical Society (LCHS) is a 501©3 non-profit organization with a mission to bring to light, preserve and promote the history of Louisa County. Our vision is to enlighten the present by illuminating the past in order to inspire the future. We share the stories of past Louisans through our museum exhibitions, educational programs, publications, research assistance, digital resources, and care of our growing collection of buildings, documents and artifacts. 

</p>		<p>Our collections offer insights into the everyday life of Louisians as well as exceptional times in the county’s history, such as during both the Revolutionary and Civil Wars, and the Civil Rights Movement. Artifacts, documents, and oral histories range from Indigenous Native groups through the 21st century, and today continue to chronicle the story of present day Louisa County. Items held in the archive range from government and court data, documentation of family, school, and church life, information pertaining to agricultural and business development, vital records, and materials detailing the experiences of enslaved and freed African Americans.

			<p>As our collection transitions to online access, staff and volunteers are continually adding, updating, and digitizing records at the Museum. We encourage the public to contact the Sargeant Museum to inquire about its holdings and to access non-digitized materials described in this system.
